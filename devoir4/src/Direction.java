

/**
 * L'énumération Direction représente les différentes directions cardinales.
 * Elle est utilisée pour indiquer les mouvements possibles dans une carte.
 */
public enum Direction {

    /**
     * Représente la direction vers le nord.
     */
    NORD,

    /**
     * Représente la direction vers le sud.
     */
    SUD,

    /**
     * Représente la direction vers l'ouest.
     */
    OUEST,

    /**
     * Représente la direction vers l'est.
     */
    EST
}
